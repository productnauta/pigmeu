function aiomatic_get_omniblock_object($realtime_mode = false)
{
    $aiomatic_Chatbot_Settings = get_option('aiomatic_Chatbot_Settings', false);
    if (isset($aiomatic_Chatbot_Settings['god_omniblock_rules']) && !empty(trim($aiomatic_Chatbot_Settings['god_omniblock_rules'])))
    {
        $omniblock_descriptions = [];
        $omni_arr = preg_split('/\r\n|\r|\n/', trim($aiomatic_Chatbot_Settings['god_omniblock_rules']));
        $omni_arr = array_map('trim', $omni_arr);
        $omni_arr = array_filter($omni_arr);
        foreach ($omni_arr as $line) {
            if (strpos($line, '=>') !== false) {
                [$id, $desc] = array_map('trim', explode('=>', $line, 2));
                if (!empty($id) && !empty($desc)) {
                    $omniblock_descriptions[] = "`$id`: $desc";
                }
            }
        }
        $allowed_commands = !empty($omniblock_descriptions)
        ? "\n\nAvailable OmniBlock IDs:\n" . implode("\n", $omniblock_descriptions)
        : "\n\nNo OmniBlocks defined.";
    }
    else
    {
        $allowed_commands = '\n\nNo OmniBlocks defined.';
    }
    $return = new Aiomatic_Query_Function(
        'aiomatic_omniblock',
        'You can use this function to execute OmniBlocks, which are defined in the Aiomatic settings. The OmniBlock ID is required. Next I will provide you the OmniBlock IDs and their description, which you will be able to execute (each on a new line).' . $allowed_commands,
        [
            new Aiomatic_Query_Parameter('omniblock_id', 'The OmniBlock ID which will be executed. Choose from the available IDs.', 'string', true),
            new Aiomatic_Query_Parameter('input_parameters', 'Add any input parameter you want to pass to the OmniBlock execution queue. You can also leave this empty. You need to provide the input parameters as a valid JSON, like in this example: {"parameter_name":"parameter_value"} ', 'string', true)
        ], 
        $realtime_mode
    );
    return $return;
}
