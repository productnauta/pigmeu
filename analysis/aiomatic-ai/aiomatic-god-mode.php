function aiomatic_check_god_functions_and_add($function_name, $aiomatic_Chatbot_Settings, $god_extension, $function_call, &$return_arr)
{
    if(in_array($function_name, $god_extension))
    {
        if(function_exists($function_call))
        {
            $return_arr[] = array('type' => 'function', 'function' => $function_call());
        }
    }
    else
    {
        if (isset($aiomatic_Chatbot_Settings[$function_name]) && trim($aiomatic_Chatbot_Settings[$function_name]) == 'on' && function_exists($function_call))
        {
            $return_arr[] = array('type' => 'function', 'function' => $function_call());
        }
    }
}
